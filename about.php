		<p>The household population of San Roque in the 2015 Census was 6,745 broken down into 1,434 households or an average of 4.70 members per household.</p>
		<p>According to the 2015 Census, the age group with the highest population in San Roque is 5 to 9, with 661 individuals. Conversely, the age group with the lowest population is 80 and over, with 57 individuals.</p>
		<p>Combining age groups together, those aged 14 and below, consisting of the young dependent population which include infants/babies, children and young adolescents/teenagers, make up an aggregate of 27.72% (1,870). Those aged 15 up to 64, roughly, the economically active population and actual or potential members of the work force, constitute a total of 66.30% (4,472). Finally, old dependent population consisting of the senior citizens, those aged 65 and over, total 5.97% (403) in all.</p>
		<p>The computed Age Dependency Ratios mean that among the population of San Roque, there are 42 youth dependents to every 100 of the working age population; there are 9 aged/senior citizens to every 100 of the working population; and overall, there are 51 dependents (young and old-age) to every 100 of the working population.</p>
		<p>The median age of 27 indicates that half of the entire population of San Roque are aged less than 27 and the other half are over the age of 27.</p>
